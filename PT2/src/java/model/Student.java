package model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Student {

    private String ID;
    private String name;
    private Date dob;
    private int gender;
    private String address;

    public Student() {
    }

    public Student(String ID, String name, Date dob, int gender, String address) {
        this.ID = ID;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.address = address;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    

}
