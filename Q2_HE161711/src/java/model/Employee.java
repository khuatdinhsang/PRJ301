package model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Employee {
    private String  id;
    private String name;
    private Date dob;
    private boolean gender;
    private String createdBy;

    public Employee() {
    }

    public Employee(String id, String name, Date dob, boolean gender, String createdBy) {
        this.id = id;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.createdBy = createdBy;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }
    
}
