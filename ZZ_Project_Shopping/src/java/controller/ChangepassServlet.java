    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("change.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        String id_raw = request.getParameter("id");
        String oldPass = request.getParameter("opass");
        String password = request.getParameter("npass");
        String rePassword = request.getParameter("rpass");
        int id = Integer.parseInt(id_raw);
        AccountDAO aD = new AccountDAO();
        Account a = aD.getAccountID(id);
        if (!aD.getPasswordById(id).equals(oldPass)) {
            request.setAttribute("mess", "password cua ban khong chinh xac");
            request.getRequestDispatcher("change.jsp").forward(request, response);
        }
        if (!password.equals(rePassword)) {
            request.setAttribute("mess", "password khong trung nhau");
            request.getRequestDispatcher("change.jsp").forward(request, response);
        } else {
            aD.changePassword(password, a);
            session.setAttribute("account", a);
        }

        System.out.println("dsadsad");
        response.sendRedirect("userprofile");
    }

