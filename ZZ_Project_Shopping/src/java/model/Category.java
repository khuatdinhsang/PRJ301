package model;

/**
 *
 * @author Admin
 */
public class Category {
    private int cid;
    private String name;
    private int quantity;

    public Category() {
    }

    public Category(int cid, String name, int quantity) {
        this.cid = cid;
        this.name = name;
        this.quantity = quantity;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
}
